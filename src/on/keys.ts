export const MouseEventKey = [
    'click', // 鼠标单击事件，当用户单击页面元素时触发
    'dblclick', // 鼠标双击事件，当用户双击页面元素时触发
    'mouseup', // 鼠标按下事件，当用户按下鼠标按钮时触发
    'mousedown', // 鼠标释放事件，当用户释放鼠标按钮时触发
    'mousemove', // 鼠标移动事件，当用户移动鼠标时触发
    'mouseover', // 鼠标移入事件，当用户将鼠标移动到一个页面元素上时触发
    'mouseout',
    // 鼠标移出事件，当用户将鼠标移出一个页面元素时触发

    // 往下的不常见的元素
    'contextmenu', // 上下文菜单事件，当用户右键单击页面元素时触发。
    'mouseenter', // 鼠标进入，但不包含子元素
    'mouseleave', // 鼠标离开，但不包含子元素

    'mousewheel', // 鼠标滚轮事件
    'DOMMouseScroll', // 鼠标滚轮事件（仅限 Firefox 浏览器），当用户使用鼠标滚轮时触发
    'pointerdown', // 指针按下事件，当用户使用触摸屏或笔时触发
    'pointerup', // 指针释放事件，当用户释放触摸屏或笔时触发
    'pointermove', // 指针移动事件，当用户使用触摸屏或笔移动时触发
]


